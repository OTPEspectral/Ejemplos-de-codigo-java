package unidad1Programacion;

public class ejemplo1inicial {
	
	/**
	 * Este programa coge dos numeros y los suma
	 * @param args
	 */

	public static void main(String[] args) {
		//Definicion de variables
		//Numeros enteros para la suma
		int num1;
		int num2;
		int suma;
		
		//Configuramos la suma a 0 para poder calcular la suma
		suma=0;
		
		//Le asignamos los valores a num1 y num2
		num1=4;
		num2=6;
		
		//Sumamos los dos num
		suma=num1+num2; 
		
		System.out.println(suma);

	}

}
